package dev.sgp.entite;

public class Statistique {

	
	int nomVisites;
	String chemin;
	long min;
	long max;
	double moyenne;
	
	
	public int getNomVisites() {
		return nomVisites;
	}
	public void setNomVisites(int nomVisites) {
		this.nomVisites = nomVisites;
	}
	public String getChemin() {
		return chemin;
	}
	public void setChemin(String chemin) {
		this.chemin = chemin;
	}
	public long getMin() {
		return min;
	}
	public void setMin(long min) {
		this.min = min;
	}
	public long getMax() {
		return max;
	}
	public void setMax(long max) {
		this.max = max;
	}
	public double getMoyenne() {
		return moyenne;
	}
	public void setMoyenne(double moyenne) {
		this.moyenne = moyenne;
	}
	
	
	
}
